package com.forgerock.openbanking.common.model.as.registration;

public class OBContact {
    public String email;
    public String name;
    public String type;
    public String phone;

    public OBContact(String email, String name, String type, String phone) {
        this.email = email;
        this.name = name;
        this.type = type;
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
